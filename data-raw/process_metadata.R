# --- Normalization Helper ---
normalize_str <- function(s) {
  # Replace common umlauts with ASCII equivalents
  s <- str_replace_all(s, "\u00e4", "ae") |> str_replace_all("\u00f6", "oe") |> str_replace_all("\u00fc", "ue")
  s <- str_replace_all(s, "\u00c4", "ae") |> str_replace_all("\u00d6", "oe") |> str_replace_all("\u00dc", "ue")
  s <- str_replace_all(s, "\u00df", "ss")
  # Handle decomposed characters (a + diaeresis)
  s <- iconv(s, to = "ASCII//TRANSLIT")
  return(s)
}

