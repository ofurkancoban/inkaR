# Normalize dictionary: everything to lower case and normalized ASCII
dict_norm <- dict
names(dict_norm) <- str_to_lower(normalize_str(names(dict)))
dict_norm <- dict_norm[!duplicated(names(dict_norm))]

# Sort by length DESC to match longer phrases first
dict_sorted <- dict_norm[order(nchar(names(dict_norm)), decreasing = TRUE)]

# Final Translation Function
translate_final <- function(txt_vec) {
  if (length(txt_vec) == 0) return(character(0))
  
  # 1. Normalize all to ASCII
  res <- normalize_str(txt_vec)
  
  # 2. Iterative replacement
  for (i in seq_along(dict_sorted)) {
    orig <- names(dict_sorted)[i]
    repl <- dict_sorted[i]
    
    # We use manual lookaround boundaries because \\b is unreliable in some R environments
    # We escape any dots in the original key for regex
    orig_esc <- str_replace_all(orig, "\\.", "\\\\.")
    pat <- paste0("(?i)(?<=^|[^a-zA-Z0-9])", orig_esc, "(?=[^a-zA-Z0-9]|$)")
    res <- str_replace_all(res, regex(pat), repl)
  }
  
  # Final Polish
  res <- str_replace_all(res, "\\[Auto-EN\\]", "")
  res <- str_replace_all(res, regex("\\bab\\b ([0-9]+)", ignore_case = TRUE), "aged $1 and older")
  # Fix common abbreviation artifacts from the tokenizer/regex
  res <- str_replace_all(res, "and\\.", "and")
  res <- str_replace_all(res, "without\\.", "without")
  res <- str_replace_all(res, "\\s+", " ")
  res <- str_trim(res)
  
  # Capitalize first letter of name
  res <- paste0(str_to_upper(str_sub(res, 1, 1)), str_sub(res, 2))
  
  return(res)
}

